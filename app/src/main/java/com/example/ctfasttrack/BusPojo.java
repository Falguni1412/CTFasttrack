package com.example.ctfasttrack;

public class BusPojo {

    String id;
    String from;

    String to;
    String number;
    String driver_name;
    String driver_mobileno;
    String conductor_name;
    String image_url;
    String image_name;
    String status;
    String updated_at;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getDriver_name() {
        return driver_name;
    }

    public void setDriver_name(String driver_name) {
        this.driver_name = driver_name;
    }

    public String getDriver_mobileno() {
        return driver_mobileno;
    }

    public void setDriver_mobileno(String driver_mobileno) {
        this.driver_mobileno = driver_mobileno;
    }

    public String getConductor_name() {
        return conductor_name;
    }

    public void setConductor_name(String conductor_name) {
        this.conductor_name = conductor_name;
    }

    public String getImage_url() {
        return image_url;
    }

    public void setImage_url(String image_url) {
        this.image_url = image_url;
    }

    public String getImage_name() {
        return image_name;
    }

    public void setImage_name(String image_name) {
        this.image_name = image_name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }
}
